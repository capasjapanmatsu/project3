import { createContext, useContext, useEffect, useState, ReactNode, useCallback } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '../utils/supabase';
import { safeSetItem, safeGetItem } from '../utils/safeStorage';

interface UserProfile {
  id: string;
  user_type: string;
  created_at: string;
  name?: string;
  email?: string;
  [key: string]: unknown;
}

interface AuthContextType {
  user: User | null;
  session: Session | null;
  loading: boolean;
  logout: () => Promise<void>;
  isAuthenticated: boolean;
  signInWithMagicLink: (email: string) => Promise<{ success: boolean; error?: string }>;
  verify2FA: (code: string) => Promise<{ success: boolean; error?: string }>;
  setIsTrustedDevice: (trusted: boolean) => void;
  isAdmin: boolean;
  userProfile: UserProfile | null;
}

const AuthContext = createContext<AuthContextType>({
  user: null,
  session: null,
  loading: true,
  logout: async () => { await Promise.resolve(); },
  isAuthenticated: false,
  signInWithMagicLink: async () => { await Promise.resolve(); return { success: false }; },
  verify2FA: async () => { await Promise.resolve(); return { success: false }; },
  setIsTrustedDevice: () => {},
  isAdmin: false,
  userProfile: null,
});

const AuthProvider = ({ children }: { children: ReactNode }) => {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);
  const [isAuthenticated, setIsAuthenticated] = useState(false);
  const [isAdmin, setIsAdmin] = useState(false);
  const [userProfile, setUserProfile] = useState<UserProfile | null>(null);

  const fetchUserProfile = async (userId: string, userEmail?: string): Promise<UserProfile | null> => {
    try {
      const { data: profile, error } = await supabase
        .from('profiles')
        .select('*')
        .eq('id', userId)
        .single();

      if (error) {
        // „Éó„É≠„Éï„Ç°„Ç§„É´„ÅåÂ≠òÂú®„Åó„Å™„ÅÑÂ†¥Âêà„ÅÆ„Åø„ÄÅËá™Âãï‰ΩúÊàê„ÇíË©¶„Åø„Çã
        if (error.code === 'PGRST116' && userEmail) {
          try {
            const profileData = {
              id: userId,
              name: userEmail.split('@')[0],
              user_type: userEmail === 'capasjapan@gmail.com' ? 'admin' : 'user',
              created_at: new Date().toISOString()
            };
            
            const { data: newProfile, error: createError } = await supabase
              .from('profiles')
              .insert([profileData])
              .select()
              .single();
            
            if (createError) {
              console.error('Error creating profile:', createError);
              return null;
            }
            
            return newProfile as UserProfile;
          } catch (createError) {
            console.error('Exception creating profile:', createError);
            return null;
          }
        }
        
        console.error('Error fetching user profile:', error.message);
        return null;
      }
      
      return profile as UserProfile;
    } catch (error) {
      console.error('Exception in fetchUserProfile:', error);
      return null;
    }
  };

  const checkAdminStatus = (user: User | null, profile: UserProfile | null): boolean => {
    if (!user) {
      return false;
    }
    
    const isAdminByEmail = user.email === 'capasjapan@gmail.com';
    const isAdminByProfile = profile?.user_type === 'admin';
    const isAdmin = isAdminByEmail || isAdminByProfile;
    
    return isAdmin;
  };

  useEffect(() => {
    let isMounted = true;
    let timeoutId: ReturnType<typeof setTimeout>;
    
    const initializeAuth = async () => {
      try {
        // Êú¨Áï™Áí∞Â¢É„Åß„ÅÆÊúÄÈÅ©Âåñ: „Çà„ÇäÁü≠„ÅÑ„Çø„Ç§„É†„Ç¢„Ç¶„Éà„Å®Êó©Êúü„ÅÆ„Éï„Ç©„Éº„É´„Éê„ÉÉ„ÇØ
        const timeoutDuration = import.meta.env.PROD ? 8000 : 5000; // Êú¨Áï™Áí∞Â¢É„Åß„ÇÇ8Áßí„Å´Áü≠Á∏Æ
        
        timeoutId = setTimeout(() => {
          if (isMounted) {
            console.warn(`‚è±Ô∏è Auth initialization timeout after ${timeoutDuration}ms, falling back to logged out state`);
            
            setSession(null);
            setUser(null);
            setIsAuthenticated(false);
            setUserProfile(null);
            setIsAdmin(false);
            setLoading(false);
          }
        }, timeoutDuration);

        // Supabase„Çª„ÉÉ„Ç∑„Éß„É≥ÂèñÂæó„ÅÆÂâç„Å´„Éç„ÉÉ„Éà„ÉØ„Éº„ÇØÁä∂ÊÖã„Çí„ÉÅ„Çß„ÉÉ„ÇØ
        if (!navigator.onLine) {
          console.warn('‚ö†Ô∏è Network is offline, using cached state');
          // „Ç™„Éï„É©„Ç§„É≥ÊôÇ„ÅØ„É≠„Éº„Ç´„É´„Çπ„Éà„É¨„Éº„Ç∏„Åã„ÇâÂæ©ÂÖÉ„ÇíË©¶„Åø„Çã
          const cachedUser = safeGetItem('sb-auth-user');
          if (cachedUser && isMounted) {
            try {
              const user = JSON.parse(cachedUser);
              setUser(user);
              setIsAuthenticated(true);
              setLoading(false);
              return;
            } catch (e) {
              console.warn('Failed to parse cached user');
            }
          }
        }

        const { data: { session }, error } = await supabase.auth.getSession();
        
        // „Çø„Ç§„É†„Ç¢„Ç¶„Éà„Çí„ÇØ„É™„Ç¢
        if (timeoutId) clearTimeout(timeoutId);
        
        if (error) {
          console.warn('Session retrieval error:', error.message);
          
          // „Ç®„É©„Éº„Åå„ÅÇ„Å£„Å¶„ÇÇ„Ç¢„Éó„É™„Çí„ÇØ„É©„ÉÉ„Ç∑„É•„Åï„Åõ„Å™„ÅÑ
          // „É≠„Ç∞„Ç¢„Ç¶„ÉàÁä∂ÊÖã„Å®„Åó„Å¶Á∂öË°å
          if (isMounted) {
            setSession(null);
            setUser(null);
            setIsAuthenticated(false);
            setUserProfile(null);
            setIsAdmin(false);
            setLoading(false);
          }
          return;
        }
        
        if (!isMounted) return;
        
        setSession(session);
        setUser(session?.user ?? null);
        setIsAuthenticated(!!session?.user);

        if (session?.user) {
          // „Çª„ÉÉ„Ç∑„Éß„É≥ÊÉÖÂ†±„Çí„Ç≠„É£„ÉÉ„Ç∑„É•
          safeSetItem('sb-auth-user', JSON.stringify(session.user));
          
          try {
            const profile = await fetchUserProfile(session.user.id, session.user.email);
            if (isMounted) {
              setUserProfile(profile);
              const adminStatus = checkAdminStatus(session.user, profile);
              setIsAdmin(adminStatus);
            }
          } catch (profileError) {
            console.warn('Profile fetch error:', profileError instanceof Error ? profileError.message : 'Unknown error');
            
            // „Éó„É≠„Éï„Ç£„Éº„É´ÂèñÂæó„Å´Â§±Êïó„Åó„Å¶„ÇÇ„Çª„ÉÉ„Ç∑„Éß„É≥„ÅØÊúâÂäπ„Å®„Åó„Å¶Á∂öË°å
            if (isMounted) {
              setUserProfile(null);
              setIsAdmin(false);
            }
          }
        } else {
          // „É≠„Ç∞„Ç¢„Ç¶„ÉàÊôÇ„ÅØ„Ç≠„É£„ÉÉ„Ç∑„É•„Çí„ÇØ„É™„Ç¢
          safeSetItem('sb-auth-user', '');
          
          if (isMounted) {
            setUserProfile(null);
            setIsAdmin(false);
          }
        }
        
      } catch (error) {
        console.error('üí• Error initializing auth:', error);
        
        // Êú¨Áï™Áí∞Â¢É„Åß„ÅÆÂàùÊúüÂåñ„Ç®„É©„ÉºË©≥Á¥∞„ÇíË®òÈå≤
        if (import.meta.env.PROD) {
          console.error('Production auth initialization error:', {
            error: error instanceof Error ? error.message : 'Unknown error',
            stack: error instanceof Error ? error.stack : undefined,
            url: window.location.href,
            timestamp: new Date().toISOString(),
            userAgent: navigator.userAgent
          });
        }
        
        // Ëá¥ÂëΩÁöÑ„Å™„Ç®„É©„Éº„ÅåÁô∫Áîü„Åó„ÅüÂ†¥Âêà„ÇÇ„É≠„Ç∞„Ç¢„Ç¶„ÉàÁä∂ÊÖã„Å®„Åó„Å¶Á∂öË°å
        if (isMounted) {
          setSession(null);
          setUser(null);
          setIsAuthenticated(false);
          setUserProfile(null);
          setIsAdmin(false);
        }
      } finally {
        // „Çø„Ç§„É†„Ç¢„Ç¶„Éà„Çí„ÇØ„É™„Ç¢
        if (timeoutId) clearTimeout(timeoutId);
        // „Ç®„É©„Éº„ÅÆÊúâÁÑ°„Å´Èñ¢„Çè„Çâ„ÅöÂøÖ„Åö„É≠„Éº„Éá„Ç£„É≥„Ç∞„ÇíÁµÇ‰∫Ü
        if (isMounted) {
          setLoading(false);
          if (import.meta.env.PROD) {
            console.log('üèÅ Production auth initialization completed');
          }
        }
      }
    };

    // ÊúÄÂ§ßÂæÖÊ©üÊôÇÈñì„ÅÆÁ∑äÊÄ•„Éï„Ç©„Éº„É´„Éê„ÉÉ„ÇØÔºàÊú¨Áï™Áí∞Â¢É: 15Áßí„ÄÅÈñãÁô∫Áí∞Â¢É: 10ÁßíÔºâ
    const emergencyTimeoutDuration = import.meta.env.PROD ? 15000 : 10000;
    
    const emergencyTimeout = setTimeout(() => {
      if (isMounted && loading) {
        console.warn(`‚ö†Ô∏è Emergency timeout after ${emergencyTimeoutDuration}ms: Force completing auth initialization`);
        setLoading(false);
      }
    }, emergencyTimeoutDuration);

    void initializeAuth();

    const { data: { subscription } } = supabase.auth.onAuthStateChange(async (event, session) => {
      if (!isMounted) return;
      
      setSession(session);
      setUser(session?.user ?? null);
      setIsAuthenticated(!!session?.user);

      if (session?.user) {
        // „Çª„ÉÉ„Ç∑„Éß„É≥ÊÉÖÂ†±„Çí„Ç≠„É£„ÉÉ„Ç∑„É•
        safeSetItem('sb-auth-user', JSON.stringify(session.user));
        
        try {
          const profile = await fetchUserProfile(session.user.id, session.user.email);
          if (isMounted) {
            setUserProfile(profile);
            const adminStatus = checkAdminStatus(session.user, profile);
            setIsAdmin(adminStatus);
          }
        } catch (profileError) {
          console.warn('Profile fetch error in auth state change:', profileError instanceof Error ? profileError.message : 'Unknown');
          if (isMounted) {
            setUserProfile(null);
            setIsAdmin(false);
          }
        }
      } else {
        // „É≠„Ç∞„Ç¢„Ç¶„ÉàÊôÇ„ÅØ„Ç≠„É£„ÉÉ„Ç∑„É•„Çí„ÇØ„É™„Ç¢
        safeSetItem('sb-auth-user', '');
        
        if (isMounted) {
          setUserProfile(null);
          setIsAdmin(false);
        }
      }
      
      // Auth state change„ÅåÁô∫Áîü„Åó„Åü„Çâloading„ÇíÁµÇ‰∫Ü
      if (isMounted) {
        setLoading(false);
      }
    });

    return () => {
      isMounted = false;
      if (timeoutId) clearTimeout(timeoutId);
      clearTimeout(emergencyTimeout);
      subscription.unsubscribe();
    };
  }, []); // Á©∫„ÅÆ‰æùÂ≠òÈÖçÂàó„ÅßÂàùÂõû„ÅÆ„ÅøÂÆüË°å

  const signInWithMagicLink = useCallback(async (email: string): Promise<{ success: boolean; error?: string }> => {
    try {
      const { error } = await supabase.auth.signInWithOtp({
        email,
        options: {
          emailRedirectTo: `${window.location.origin}/dashboard`,
        },
      });
      if (error) {
        console.error('Magic Link error:', error);
        return { success: false, error: `Magic Link„ÅÆÈÄÅ‰ø°„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error.message}` };
      }
      return { success: true };
    } catch (error) {
      console.error('Magic Link error:', error);
      return { success: false, error: `Magic Link„ÅÆÈÄÅ‰ø°„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error instanceof Error ? error.message : 'Unknown error'}` };
    }
  }, []);

  const verify2FA = useCallback(async (code: string): Promise<{ success: boolean; error?: string }> => {
    try {
      if (!user) {
        return { success: false, error: '„É¶„Éº„Ç∂„Éº„ÅåË™çË®º„Åï„Çå„Å¶„ÅÑ„Åæ„Åõ„Çì' };
      }
      const { data: { session } } = await supabase.auth.getSession();
      if (!session?.access_token) {
        return { success: false, error: 'Ë™çË®º„ÅåÂøÖË¶Å„Åß„Åô' };
      }
      const response = await fetch(`${import.meta.env.VITE_SUPABASE_URL}/functions/v1/verify-2fa`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${session.access_token}`,
        },
        body: JSON.stringify({
          userId: user.id,
          code
        }),
      });
      if (!response.ok) {
        const errorData = await response.json() as { error?: string };
        return { success: false, error: errorData.error || 'Ë™çË®º„Å´Â§±Êïó„Åó„Åæ„Åó„Åü' };
      }
      const result = await response.json() as { success: boolean; error?: string };
      return { 
        success: result.success, 
        ...(result.error && { error: result.error })
      };
    } catch (error) {
      console.error('2FA verification error:', error);
      return { success: false, error: error instanceof Error ? error.message : 'Unknown error' };
    }
  }, [user]);

  const setIsTrustedDevice = useCallback((trusted: boolean) => {
    if (trusted && user) {
      safeSetItem(`trusted_device_${user.id}`, 'true');
    } else if (user) {
      safeSetItem(`trusted_device_${user.id}`, 'false');
    }
  }, [user]);

  const logout = useCallback(async () => {
    try {
      await supabase.auth.signOut();
      setIsAuthenticated(false);
      setUser(null);
      setSession(null);
      setUserProfile(null);
      setIsAdmin(false);
    } catch (error) {
      console.error('Logout error:', error);
      throw error;
    }
  }, []);

  return (
    <AuthContext.Provider value={{
      user,
      session,
      loading,
      logout,
      isAuthenticated,
      signInWithMagicLink,
      verify2FA,
      setIsTrustedDevice,
      isAdmin,
      userProfile
    }}>
      {children}
    </AuthContext.Provider>
  );
};

const useAuth = () => useContext(AuthContext);

export default useAuth;
export { AuthContext, AuthProvider };
